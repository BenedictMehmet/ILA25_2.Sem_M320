using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ILA25_2.Sem_M320._26_KW18
{
    internal class Aufgaben
    {
        /*
Aufgabe 1 – Quiz mit Punkten pro Frage

Erstelle ein Quiz mit mindestens 5 Fragen.

Anforderungen:
- Verwende List<string> für Fragen und Antworten.
- Jede Frage gibt 2 Punkte bei richtiger Antwort.
- Verwende eine Schleife für alle Fragen.

Am Ende:
- Gib die Gesamtpunktzahl aus.
- Gib aus, wie viele Fragen richtig beantwortet wurden.
*/


        /*
        Aufgabe 2 – Quiz mit Sofort-Feedback

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Speichere Fragen und Antworten in Listen.
        - Nach jeder Antwort soll sofort ausgegeben werden, ob sie richtig oder falsch ist.
        - Zähle die Punkte mit.

        Am Ende:
        - Gib die Gesamtpunktzahl aus.
        */


        /*
        Aufgabe 3 – Quiz mit Wiederholung falscher Fragen

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Speichere Fragen und Antworten in Listen.
        - Speichere alle falsch beantworteten Fragen separat.

        Am Ende:
        - Gib alle falsch beantworteten Fragen nochmals aus.
        */


        /*
        Aufgabe 4 – Quiz mit Benutzername

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Frage zuerst den Namen des Benutzers ab.
        - Verwende Listen für Fragen und Antworten.
        - Zähle die Punkte.

        Am Ende:
        - Gib aus: "Name, du hast X Punkte erreicht."
        */


        /*
        Aufgabe 5 – Quiz mit mehreren Versuchen

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Jede Frage darf maximal 2-mal beantwortet werden.
        - Wenn sie beim zweiten Versuch richtig ist, gibt es 1 Punkt.
        - Beim ersten Versuch gibt es 2 Punkte.

        Am Ende:
        - Gib die Gesamtpunktzahl aus.
        */


        /*
        Aufgabe 6 – Quiz mit Kategorien

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Erstelle zusätzlich eine Liste für Kategorien (z.B. Mathe, Allgemeinwissen).
        - Jede Frage hat eine Kategorie.
        - Gib vor jeder Frage die Kategorie aus.

        Am Ende:
        - Gib die Gesamtpunktzahl aus.
        */


        /*
        Aufgabe 7 – Quiz mit Zeit (optional einfach)

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Miss die Zeit für das gesamte Quiz (optional).
        - Verwende Listen für Fragen und Antworten.

        Am Ende:
        - Gib die Gesamtpunktzahl aus.
        - Gib zusätzlich die benötigte Zeit aus.
        */


        /*
        Aufgabe 8 – Quiz mit Prozentanzeige pro Frage

        Erstelle ein Quiz mit mindestens 5 Fragen.

        Anforderungen:
        - Verwende Listen für Fragen und Antworten.
        - Speichere, ob jede Frage richtig oder falsch war.

        Am Ende:
        - Gib für jede Frage aus:
          "Frage 1: richtig (100%)"
          "Frage 2: falsch (0%)"
        - Gib zusätzlich die gesamte Erfolgsquote aus.
        */
    }
}
